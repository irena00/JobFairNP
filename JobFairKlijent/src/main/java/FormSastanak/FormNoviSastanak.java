    private void popuniKompanije() {

        try {
            ArrayList<Kompanija> kompanije = ClientController.getInstance().getAllKompanija();

            cmbKompanija.removeAllItems();

            for (Kompanija kompanija : kompanije) {
                cmbKompanija.addItem(kompanija);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviSastanak.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
