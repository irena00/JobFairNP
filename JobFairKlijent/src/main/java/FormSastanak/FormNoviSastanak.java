    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtDatumVreme.getText().isEmpty() || txtLokacija.getText().isEmpty()
                    || txtTema.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
            Date datumVreme = sdf.parse(txtDatumVreme.getText());
            String tema = txtTema.getText();
            String lokacija = txtLokacija.getText();
            Kompanija kompanija = (Kompanija) cmbKompanija.getSelectedItem();

            Sastanak s = new Sastanak(null, datumVreme, tema, lokacija, kompanija,
                    Session.getInstance().getUlogovani());

            ClientController.getInstance().addSastanak(s);
            MainForm mf = (MainForm) getParent();
            mf.popuniSastanke();
            JOptionPane.showMessageDialog(this, "Uspesno dodat sastanak.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

