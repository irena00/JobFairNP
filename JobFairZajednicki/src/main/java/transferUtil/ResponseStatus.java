package transferUtil;

/**
 * The ResponseStatus enumeration represents the possible status values for a response.
 * 
 * <p>
 * It can indicate whether a certain operation was successful or if an error occurred.
 * </p>
 * 
 * @see Response
 * @author Irena Randjelovic
 */
public enum ResponseStatus {
	/**
     * Indicates a successful response status.
     */
    Success,

    /**
     * Indicates an error response status.
     */
    Error
}
